MIME subtype name : Vendor Tree - vnd.dart

Required parameters : none

Optional parameters : none

Encoding considerations : 8bit

Security considerations :
The media type doesn't provide any sort of internal integrity or
confidentiality protection. If these services are needed they must be
layered on top of the content. In particular, clients that receive this
mime type may execute the enclosed code, which can potentially access
arbitrary objects. It is therefore the responsibility of the client to
implement appropriate security policies, which are outside the scope of
the Dart language. The Isolates document on dartlang.org discusses the
Dart security model.

